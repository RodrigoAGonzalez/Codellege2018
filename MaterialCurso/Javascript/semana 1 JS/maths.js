function addNumbers( a, b )
{
    var suma = a + b;
    return suma;
}

function subNumbers( a, b )
{
    var suma = a - b;
    return suma;
}

function multNumbers( a, b )
{
    var suma = a * b;
    return suma;
}

function divideNumbers( a, b )
{
    var suma = a / b;
    return suma;
}